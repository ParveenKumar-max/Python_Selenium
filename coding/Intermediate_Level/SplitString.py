# Split String

value = input("Enter any string: ")
split_text = value.split()

print(f"Actual value is '{value}' and split value is {split_text}")

# it will print in list

# Actual value is 'How do you split a string into a list of words?'
# and split value is ['How', 'do', 'you', 'split', 'a', 'string', 'into', 'a', 'list', 'of', 'words?']
